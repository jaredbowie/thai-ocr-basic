(defn pattern-maker [pattern-string]
  (let [each-line
        (clojure.string/split-lines pattern-string)]
                                        ;(map #())
    (clojure.pprint/pprint (vec (for [one-line each-line]
                                  (vec (map #(Character/getNumericValue %) one-line)))))
    )
  )

